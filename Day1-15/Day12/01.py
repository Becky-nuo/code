#参数的类型（位置参数、默认值参数、命名参数）


def test01(a,b,c,d):
    print("{0}-{1}-{2}-{3}".format(a,b,c,d))


def test02(a,b,c=10,d=15):    #默认值参数，必须位于普通位置参数后面（参数在传递时是可选的）
    print("{0}-{1}-{2}-{3}".format(a,b,c,d))


test01(10,20,30,40)  #位置参数（默认按位置顺序传递，需要个数和形参匹配）
#test01(10,20)  #参数个数不匹配，报错


test01(d=20,b=40,a=10,c=100)   #命名参数，通过形参名称传递参数


test02(2,3)    #2和3指的是a,b
test02(2,3,4)   #4指的是c
